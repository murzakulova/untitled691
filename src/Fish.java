public class Fish {
    private String name;
    private String color;

    public void water() {
        System.out.println("Fish live under water");
    }

    public Fish() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
